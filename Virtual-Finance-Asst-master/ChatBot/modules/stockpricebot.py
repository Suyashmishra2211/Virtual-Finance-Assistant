    def get_stock_price(self, ticker_symbol):
        ticker = Ticker(ticker_symbol)
        todays_data = ticker.history(period='1d')
        if todays_data.empty:
            return f"No data available for {ticker_symbol}."
        else:
            return f"The current price of {self.ticker_company_map[ticker_symbol[:-3]]} is {todays_data['Close'][0]:.2f}."

    def find_ticker_symbol(self, query):
        matches = []
        for company, ticker in self.company_ticker_map.items():
            if query.lower() in company.lower() or query.upper() in ticker:
                matches.append(ticker)
        return matches

    def load_trigger_phrases(self, file_path):
        with open(file_path, 'r') as file:
            return [line.strip() for line in file.readlines()]

    def respond_to_user(self, user_input):
        match = None
        for trigger in self.trigger_phrases:
            match = re.search(fr"{trigger} (\w+)", user_input.lower())
            if match:
                break
        if match:
            query = match.group(1)
            ticker_symbols = self.find_ticker_symbol(query)
            if ticker_symbols:
                responses = []
                for ticker in ticker_symbols:
                    ticker_with_extension = f"{ticker}.NS"
                    response = self.get_stock_price(ticker_with_extension)
                    responses.append(response)
                return '<br>'.join(responses)
            else:
                return "Could not find the stock price for your query."
        else:
            return "I'm here to help with stock prices. Please ask for a stock price."

